function numeric_Callback(hObject, eventdata, handles)
% hObject    handle to p1x (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Hints: get(hObject,'String') returns contents of p1x as text
%        str2double(get(hObject,'String')) returns contents of p1x as a double
    str=get(hObject,'String');
    if isempty(str2double(str))
        set(hObject,'string','0');
        %warndlg('Input must be numerical');
    end
    generate_curve(handles)
    
function generate_curve(handles)
    %disp(handles)
    bezier([str2double(get(handles.edit1,'String')), str2double(get(handles.edit2,'String')); ...
    str2double(get(handles.edit3,'String')), str2double(get(handles.edit4,'String')); ...
    str2double(get(handles.edit5,'String')), str2double(get(handles.edit6,'String')); ...
    str2double(get(handles.edit7,'String')), str2double(get(handles.edit8,'String'))])


